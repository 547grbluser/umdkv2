#include <types.h>

uint16 umdkReadWord(const uint8 *p) {
	uint16 value = *p++;
	value <<= 8;
	value |= *p;
	return value;
}

uint32 umdkReadLong(const uint8 *p) {
	uint32 value = *p++;
	value <<= 8;
    value |= *p++;
    value <<= 8;
    value |= *p++;
    value <<= 8;
    value |= *p;
	return value;
}

void umdkWriteWord(uint16 value, uint8 *p) {
	p[1] = value & 0x00FF;
	value >>= 8;
	p[0] = value & 0x00FF;
}

void umdkWriteLong(uint32 value, uint8 *p) {
	p[3] = value & 0x000000FF;
	value >>= 8;
	p[2] = value & 0x000000FF;
	value >>= 8;
	p[1] = value & 0x000000FF;
	value >>= 8;
	p[0] = value & 0x000000FF;
}
